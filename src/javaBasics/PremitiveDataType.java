package javaBasics;

public class PremitiveDataType {

	
	public static void main(String[] args)
	{
		byte a = 1;
		short b = 2;
		int c = 100;
		long d = 2225225;
		float e = 2233f;
		double f = 324324.3242344324;
		char g = 'a';
		
		System.out.println(a + " " + b + " " + c + " " + d + " " + e + " " + f+ " " + g);
	}
}